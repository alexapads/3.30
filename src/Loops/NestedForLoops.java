package Loops;

public class NestedForLoops {
    public static void main(String[] args) {

        for( int i = 0 ; i < 5 ; i++ ){

            for( int k = 0; k < 3 ; k++ ){

                System.out.println("i is: "+i);
                System.out.println("k is: "+k);
            }
        }




    }
}
